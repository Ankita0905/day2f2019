package com.lambton;

public class ArithmaticClass {
    int add(int a, int b)
    {
        int c;
        c=a+b;
        return c;
    }
    int sub(int a, int b)
    {
        int c;
        c=a-b;
        return c;
    }
    int mul(int a, int b)
    {
        int c;
        c=a*b;
        return c;
    }
    float div(int a, int b)
    {
        float c;
        c=(float)a/b;
        return c;
    }
    int mod(int a, int b)
    {
        int c;
        c=a%b;
        return c;
    }
}
